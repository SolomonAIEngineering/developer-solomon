#!/bin/bash

# Function to add .hbs extension to a file
add_hbs_extension() {
    local file="$1"
    local dir=$(dirname "$file")
    local base=$(basename "$file")

    # Rename the file by adding .hbs
    mv "$file" "${dir}/${base}.hbs"
    echo "Renamed: $file -> ${dir}/${base}.hbs"
}

# Specify the root directory path here
root_directory="."

# Use find to locate all files and apply the renaming function
find "$root_directory" -type f | while read -r file; do
    add_hbs_extension "$file"
done