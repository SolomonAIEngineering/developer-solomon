import FinancialEngine from "@solomon-ai/financial-engine-sdk";

/**
 * @fileoverview This file initializes and exports a FinancialEngine instance for
 * use throughout the application.
 */

/**
 * A configured instance of the FinancialEngine from the Solomon AI SDK.
 *
 * This engine is set up with default headers for authentication and a base URL for API requests.
 * It can be used to interact with the Solomon AI financial services platform.
 *
 * @constant
 * @type {FinancialEngine}
 *
 * @example
 * import { engine } from './utils/engine';
 *
 * // Use the engine to make API calls
 * const result = await engine.someMethod();
 *
 * @property {Object} defaultHeaders - Default headers sent with every request.
 * @property {string} defaultHeaders.x-api-key - The API key for authentication.
 * @property {string} defaultHeaders.Authorization - The Bearer token for authentication.
 * @property {string} baseURL - The base URL for all API requests.
 */
export const engine = new FinancialEngine({
  defaultHeaders: {
    "x-api-key": process.env.MIDDAY_ENGINE_API_KEY ?? "SOLOMONAI",
    Authorization: `Bearer ${process.env.MIDDAY_ENGINE_API_KEY ?? "SOLOMONAI"}`,
  },
  baseURL: process.env.ENGINE_API_ENDPOINT ?? "https://engine.solomon-ai-platform.com",
});
